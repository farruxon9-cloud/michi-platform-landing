document.addEventListener('DOMContentLoaded', () => {
    const langBtns = document.querySelectorAll('.lang-btn');
    const i18nElements = document.querySelectorAll('[data-i18n]');

    function setLanguage(lang) {
        langBtns.forEach(btn => {
            if(btn.dataset.lang === lang) {
                btn.classList.add('active');
            } else {
                btn.classList.remove('active');
            }
        });

        i18nElements.forEach(el => {
            const key = el.dataset.i18n;
            if(translations[lang] && translations[lang][key]) {
                if(el.tagName === 'META') {
                    el.setAttribute('content', translations[lang][key]);
                } else {
                    el.innerHTML = translations[lang][key];
                }
            }
        });
        
        localStorage.setItem('michi_lang', lang);
        document.documentElement.lang = lang;
        if(translations[lang]['page_title']) {
            document.title = translations[lang]['page_title'];
        }
    }

    langBtns.forEach(btn => {
        btn.addEventListener('click', (e) => {
            setLanguage(e.target.dataset.lang);
        });
    });

    const savedLang = localStorage.getItem('michi_lang');
    if(savedLang && translations[savedLang]) {
        setLanguage(savedLang);
    }

    // Handle Waitlist Form Submission
    const waitlistForm = document.getElementById('waitlist-form');
    const formMessage = document.getElementById('form-message');

    if(waitlistForm) {
        waitlistForm.addEventListener('submit', async (e) => {
            e.preventDefault();
            const email = waitlistForm.email.value;
            const submitBtn = waitlistForm.querySelector('button');
            const originalBtnText = submitBtn.innerText;

            submitBtn.disabled = true;
            submitBtn.innerText = 'Sending...';
            formMessage.innerText = '';

            try {
                // This URL is now connected to your Google Sheet
                const scriptURL = 'https://script.google.com/macros/s/AKfycbwRWVFT53dvaLrvs4JhOQzeX1f5M-4NyieKiXtZfhbaT6jM6nJC5jqqjjrIgka1V1LqdQ/exec';
                
                const response = await fetch(scriptURL, {
                    method: 'POST',
                    mode: 'no-cors',
                    headers: { 'Content-Type': 'application/x-www-form-urlencoded' },
                    body: new URLSearchParams({ email: email })
                });

                formMessage.innerText = 'Thank you! You are on the waitlist. ✓';
                formMessage.className = 'form-message success';
                waitlistForm.reset();
            } catch (error) {
                formMessage.innerText = 'Something went wrong. Please try again.';
                formMessage.className = 'form-message error';
            } finally {
                submitBtn.disabled = false;
                submitBtn.innerText = originalBtnText;
            }
        });
    }

    // Redirect all interactive buttons to Waitlist (Only on pages where the download section exists)
    const ctaButtons = document.querySelectorAll('.primary-btn, .secondary-btn, .apply-btn, .store-btn');
    ctaButtons.forEach(btn => {
        // Skip the actual submit button in the form
        if(btn.type === 'submit') return;

        // Skip anchor links pointing to page hashes
        if(btn.tagName === 'A' && btn.getAttribute('href') && btn.getAttribute('href').startsWith('#') && btn.getAttribute('href') !== '#') return;

        btn.addEventListener('click', (e) => {
            const target = document.getElementById('download');
            if(target) {
                e.preventDefault();
                target.scrollIntoView({ behavior: 'smooth' });
                // Highlight the input
                setTimeout(() => {
                    if (waitlistForm) {
                        const input = waitlistForm.querySelector('input');
                        if (input) {
                            input.focus();
                            input.style.boxShadow = '0 0 0 3px rgba(0, 102, 204, 0.3)';
                            setTimeout(() => input.style.boxShadow = '', 2000);
                        }
                    }
                }, 800);
            }
        });
    });
});
